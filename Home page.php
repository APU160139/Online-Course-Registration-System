             <h4><strong>Registration: </strong></h4>
             <p> All the new students must register their courses in the prescribed Registration Form at the time of admission.

                Existing students must register their courses of the new semester before the classes of the new semester start on payment of their semester tuition and other fees or their parts as determined by the university administration. Students should consult the Faculty in Charge/Chairman of the Department/Program Director (in case of MBA, EMBA & MBM Programs) in planning their course schedule and should follow the rules and procedures related to registration for courses.</p>
                <h4><strong>Late Registration: </strong></h4>
                <p>A student who seeks to register after the first day of classes must obtain permission from the office of the university. Those students who are given permission to register late may have to pay a late registration fee.</p>
                <h4><strong>Retake Course Registration: </strong></h4>
                <p>Students scoring 'F' grade in any course shall have to pay full tuition fees for repeating the course. Students with grade lower than "A" in any course may retake the course to improve their grades and shall have to pay half of the tuition fees for the course/courses.</p>
                <h4><strong>Re-admisson Registration: </strong></h4>
                <p>Students may repeat courses in order to improve their grades. They must register for and complete the course/courses to accomplish the repeat and 50% of the tuition fees has to be paid for the course/courses repeated. However, a student who receives "F" grade in any course must repeat that course and obtain a minimum of "D" grade in order to meet degree requirements. Both the original and the repeat grades are recorded in the transcript, but CGPA is calculated on the basis of the better of the two grades. A course can be repeated once only.</p>
                <h4><strong>Research: </strong></h4>
                <p>Students have to undertake a semester-long research under the supervision of a faculty and earn a minimum of C grade for graduation where such research is a requirement for the degree.</p>
                <h4><strong>Internship Requirement: </strong></h4>
                <p>Students registering for internship should preferably have completed all course work. Students may not be registered for any course during internship. For purposes of fulfilling degree requirements, students should be interned in organizations ideally for twelve weeks, allowing two to four weeks for writing reports, etc. The students must earn at least a C grade in the internship project.</p>
         
            
